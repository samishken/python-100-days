# Modulo Operator %  (how many is remaining after the division)
# In Python, the modulo operator is represented by the percent sign %, and it gives the remainder of a division between two numbers.
# 10 % 5 = 0
# 10 % 3 = 1
# 10 % 4 = 2
# 14 % 5 = 4
# print(14 % 5)  # 2

number_to_check = int(input("What is the number you want to check? "))
if number_to_check % 2 == 0:
    print("Even")
else:
    print("Odd")

